import openpyxl

# ==========================================
# ШАГ 1: Загрузка существующего файла
# ==========================================
try:
    wb = openpyxl.load_workbook("Фильтрация данных.xlsx")
    print("Исходный файл успешно загружен!")
except FileNotFoundError:
    print("Ошибка: Файл 'Фильтрация данных.xlsx' не найден.")
    exit()

# ==========================================
# ШАГ 2: Включение кнопок автофильтра на листе 1_1
# ==========================================
if '1_1' in wb.sheetnames:
    ws_1 = wb['1_1']
    # Просто включаем стрелочки для всей таблицы (по её границам)
    ws_1.auto_filter.ref = ws_1.dimensions
    print("Стрелочки фильтра добавлены на лист 1_1.")
else:
    print("Внимание: Лист '1_1' не найден!")

# ==========================================
# ШАГ 3: Включение кнопок автофильтра на листе 1_2
# ==========================================
if '1_2' in wb.sheetnames:
    ws_2 = wb['1_2']
    # Просто включаем стрелочки для всей таблицы
    ws_2.auto_filter.ref = ws_2.dimensions
    print("Стрелочки фильтра добавлены на лист 1_2.")
else:
    print("Внимание: Лист '1_2' не найден!")

# ==========================================
# ШАГ 4: Сохранение результата
# ==========================================
output_filename = 'Практическая_работа_5_Задание_1_Стрелочки.xlsx'
wb.save(output_filename)
print(f'Готово! Файл "{output_filename}" успешно сохранен.')